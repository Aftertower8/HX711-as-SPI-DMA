const static uint8_t clock[8] =
{ 0b00000000, 0b01010101, 0b01010101, 0b01010101, 0b01010101,
  0b01010101, 0b01010101, 0b01000000 };  // 25 taktÃ³w; clock[0]-bezpieczne opoznienie, clock[7]-gain na 128, clock[1..6] - odbieranie danych (rising edge)

